/** <!--********************************************************************-->
 *
 * @name INFO structure
 * @{
 *
 *****************************************************************************/

struct INFO {
};

static info *
MakeInfo ()
{
    info *result;

    DBUG_ENTER ("MakeInfo");

    result = MEMmalloc (sizeof (info));

    DBUG_RETURN (result);
}

static info *
FreeInfo (info *info)
{
    DBUG_ENTER ("FreeInfo");

    info = MEMfree (info);

    DBUG_RETURN (info);
}

/** <!--********************************************************************-->
 * @}  <!-- INFO structure -->
 *****************************************************************************/

/** <!--********************************************************************-->
 *
 * @name Entry functions
 * @{
 *
 *****************************************************************************/

/** <!--********************************************************************-->
 *
 * @fn node *MSSdoMoveSyncStatement( node *syntax_tree)
 *
 *****************************************************************************/

node *
MSSdoMoveSyncStatement (node *syntax_tree)
{
    info *info;
    pattern *pat;
    DBUG_ENTER ("MSSdoMoveSyncStatement");
    DBUG_PRINT ("MSS", ("Moving sync statements..."));

    info = MakeInfo ();

    TRAVpush (TR_mss);

    pat = PMprf (1, PMAisPrf (F_sync), 0);

    syntax_tree = MAdoMoveAssigns (syntax_tree, pat, FALSE);

    TRAVpop ();

    pat = PMfree (pat);

    info = FreeInfo (info);

    DBUG_RETURN (syntax_tree);
}

